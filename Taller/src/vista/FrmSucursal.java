    private void mayorVenta() {
        Double maxValor = Double.MIN_VALUE;
        String nombreSucursal = "";

        for (Sucursal sucursal : control.getSucursales()) {
            for (Venta venta : sucursal.getVentas()) {
                if (venta.getValor() > maxValor) {
                    maxValor = venta.getValor();
                    nombreSucursal = sucursal.getNombre();
                }
            }
        }
        if (maxValor != Double.MIN_VALUE) {
            JOptionPane.showMessageDialog(this, "La venta mayor es: " + maxValor + " en la sucursal: " + nombreSucursal, "Venta Mayor", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "No hay ventas registradas", "Venta Mayor", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    private void menorVenta() {
        int indice = 0;
        double minValor = Double.MAX_VALUE;
        String nombreSucursal = "";

        for (int i = 0; i < control.getSucursales().length; i++) {
            Sucursal sucursal = control.getSucursales()[i];
            if (sucursal != null) {
                double totalVentas = Utilidades.sumarVentas(sucursal);
                if (totalVentas < minValor) {
                    minValor = totalVentas;
                    nombreSucursal = sucursal.getNombre();
                    indice = i;
                }
            }
        }

        if (minValor != Double.MAX_VALUE) {
            JOptionPane.showMessageDialog(this, "La venta menor es: " + minValor + " en la sucursal: " + nombreSucursal, "Venta Menor", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "No hay ventas registradas", "Venta Menor", JOptionPane.INFORMATION_MESSAGE);
        }
    }


